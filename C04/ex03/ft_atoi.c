int	ft_atoi(char *str)
{
	int	rzl;
	int	sg;

	rzl = 0;
	sg = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sg *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		rzl = rzl * 10 + *str - '0';
		str++;
	}
	return (rzl * sg);
}
