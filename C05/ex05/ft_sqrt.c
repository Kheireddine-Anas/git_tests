int	ft_sqrt(int nb)
{
	long	in;
	long	b;

	b = nb;
	if (b <= 0)
		return (0);
	if (b == 1)
		return (1);
	in = 2;
	if (b >= 2)
	{
		while (in * in <= b)
		{
			if (in * in == b)
				return (in);
			in++;
		}
	}
	return (0);
}
